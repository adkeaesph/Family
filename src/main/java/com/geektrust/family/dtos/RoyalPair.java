package com.geektrust.family.dtos;

public class RoyalPair {
	private String kingsName;
	private String queensName;
	
	public RoyalPair() {
		
	}
	
	public String getKingsName() {
		return kingsName;
	}
	public String getQueensName() {
		return queensName;
	}
	public void setKingsName(String kingsName) {
		this.kingsName = kingsName;
	}
	public void setQueensName(String queensName) {
		this.queensName = queensName;
	}
	
	

}
